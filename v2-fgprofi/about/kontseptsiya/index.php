                        <p>Спрос на сообщество демонстрируют не только сами профессионалы в области финансовой грамотности, но и «внешние игроки» – прежде всего, <b><i>стейкхолдеры </i></b>(заинтересованные пользователи) сообщества, т.е. субъекты, на которых оказывает влияние деятельность профессионального сообщества, и которые сами могут оказывать влияние на деятельность профессионального сообщества.</p> <br>
                        <p>Различия между членами профессионального сообщества и стейкхолдерами можно провести по двум содержательным признакам:</p>
                        <ul>
                            <li>Представитель профессионального сообщества непосредственно занимается проблематикой финансовой грамотности, тогда как стейкхолдер испытывает на себе последствия этой деятельности и/или же может заметным образом влиять на ее содержание.</li>

                        </ul>

                        <ul>
                            <li>Представитель профессионального сообщества – это, прежде всего, физическое лицо, индивид, конкретный эксперт с определенными компетенциями, сферой деятельности и потребностями (профессиональными и житейскими). <br>
                                Стейкхолдер – это, как правило, та или иная организация, ведомство или группа, чье влияние определяется ее формальными и/или неформальными статусом (а не качествами работающих на него людей).<br>
                                Представитель стейкхолдера – конкретный специалист – может быть членом сообщества; стейкхолдеры в целом представляют собой ресурс пополнения сообщества.</li>
                        </ul>
                        <p>Сообщество профессионалов в области финансовой грамотности обладает многочисленными
                            и влиятельными стейкхолдерами. Успешность функционирования сообщества во многом зависит от степени его ориентированности на запросы стейкхолдеров и взаимодействия
                            с ними в рабочих вопросах. Ключевыми стейкхолдерами (заинтересованными пользователями) сообщества являются:</p>

                        <ul class="list-line">
                            <li>- <b><i>Государственные учреждения</i></b> – федеральные, региональные и муниципальные, а также
                                существующие в сфере их интересов и полномочий квазигосударственные и негосударственные институты (например, финансовый омбудсмен), которые занимаются теми или иными аспектами финансовой грамотности и защиты прав потребителей финансовых услуг.Многообразие ведомств и различный масштаб задач, с которыми они имеют дело, формируют разные по своей тематике, объему и срочности запросы к профессиональному сообществу. Общими чертами таких запросов являются:</li>
                        </ul>

                        <ul class="list-line">
                            <li>- запрос на экспертизу проектов решений, продуктов в сфере финансовой грамотности; формирование достоверной картины конкретной ситуации/проблемы;</li>
                            <li>- запрос на донесение существующих (успешных) практик работы с различными проблемными ситуациями/вызовами до специалистов, целевой аудитории Проекта;</li>
                            <li>- запрос на обобщение и анализ опыта разработки материалов, проектов в сфере финансовой грамотности, финансового просвещения различных групп населения;</li>
                            <li>- запрос на содействие достижению отчетных целевых показателей (KPI) ведомства;</li>
                            <li>- запрос на экспертное сопровождение межведомственного взаимодействия, в т.ч. на уровне Межведомственной координационной комиссии по реализации Национальной стратегии, экспертная подготовка совещаний и документов (далее – Межведомственная координационная комиссия).</li>
                        </ul>
                        <ul class="list-line">
                            <li>-<b><i> Образовательные учреждения</i></b> – школы, вузы, система профессионального образования, система повышения квалификации, а также различные методические центры и центры компетенций2, работающие так или иначе с проблематикой финансовой грамотности. Интересы этой группы стейкхолдеров связаны с:</li>
                        </ul>
                        <ul class="list-line">
                            <li>- получением актуальной экспертизы по проблематике финансовой грамотности (в т.ч. по новостной повестке) для включения в учебно-методические материалы;</li>
                            <li>- кооптацией экспертов из необразовательных сегментов сообщества для включения в образовательный процесс;</li>
                            <li>- развитием стандартов, связанных как с преподаванием финансовой грамотности, так и с повсеместным включением активностей с нею связанных в отчетные KPI преподавателей (и привлечение соответствующего финансирования);</li>
                            <li>- получением доступа к лучшим практикам в сфере преподавания финансовой грамотности.</li>
                        </ul>
                        <ul class="list-line">
                            <li>- <b><i>Центры экспертизы</i></b> – различные субъекты, как целиком (например, бизнес-ассоциации, отдельные исследовательские организации), так и их подразделения (например, кадровые службы корпораций, отдельные исследовательские подразделения консалтинговых компаний), осуществляющие консалтинг, экспертизу, производящие новые знания в сфере финансовой грамотности. Интересы этой группы стейкхолдеров связаны с:</li>
                        </ul>
                        <ul class="list-line">
                            <li>- широким внедрением экспертизы в процессы обсуждения вопросов, связанных с финансовой грамотностью и процессы принятия соответствующих решений;</li>
                            <li>- расширением «поля» финансовой грамотности в целом (например, включение в него «налоговой грамотности», «бюджетной грамотности», современных цифровых продуктов);</li>
                            <li>- упрощением доступа к материалам Проекта / информации по проблематике финансовой грамотности;</li>
                            <li>- повсеместным распространением диагностики – количественной и качественной, – связанной с финансовой грамотностью, сбором соответствующих данных, как со стороны предложения финансовых услуг (бизнес), так и стороны спроса (потребители).</li>
                        </ul>
                        <ul class="list-line">
                            <li>- <b><i>Бизнес</i></b> – понимаемый здесь именно как субъект, ориентированный на получение прибыли от продажи финансовых услуг или сервисов с ними связанных (консультации/консалтинг, соответствующее образование/сертификация). Говоря о добросовестном бизнесе с длинным горизонтом планирования, можно выделить следующие его интересы:</li>
                        </ul>
                        <ul class="list-line">
                            <li>- расчистка рынка от недобросовестных поставщиков услуг за счет различных программ профессиональной сертификации, стандартизации и внешнего мониторинга качества услуг;</li>
                            <li>- воспитание «осознанного» потребителя финансовых услуг, который, по меньшей мере, готов соотносить свои запросы в отношении бизнеса со своими (i) финансовыми возможностями и (ii) своим уровнем знаний относительно того или иного финансового продукта (и умения пользоваться им).</li>
                        </ul>
                        <ul class="list-line">
                            <li>- <b><i>Медиа</i></b> – различные средства массовой информации, а также ресурсы, по факту ими являющиеся (блоги), включая «новые медиа» в социальных сетях. Интересы этой группы стейкхолдеров помимо получения контента, интересного их аудитории, могут состоять в приобретении роли медиатора, статуса публичной площадки для взаимодействия как членов сообщества, так и стейкхолдеров.</li>
                        </ul>
                        <ul class="list-line">
                            <li>- <b><i>Международные организации (ОЭСР, Всемирный банк)</i></b> – интересы этой группы стейкхолдеров связаны с продвижением тематики финансовой грамотности в России и мире.</li>
                        </ul>
                    </div>
                </section>




                <section class="section">
                    <h3 id="ex2">2. МОДЕЛЬНЫЕ ХАРАКТЕРИСТИКИ СООБЩЕСТВА</h3>
                    <p>Анализ существующих сообществ в разных сферах деятельности позволяет выделить следующие характеристики, которые задают <b><i>модель функционирования</i></b> той или иной общности, определяют ее <b><i>устойчивость</i></b> и <b><i>успешность</i></b> в целом:</p>
                    <ul>
                        <li><b><i>Размер</i></b> (малая или большая) и <b><i>характер</i></b> (однородная или неоднородная) <b><i>группы</i></b>, о консолидации которой идет речь.</li>
                        <li><b><i>Наличие сервисов</i></b> для своих членов и/или не только для них – имеют системообразующее значение для построения сообщества, т.к. тесно связаны со стимулами вступать в него. Принципиально важным являются:</li>
                    </ul>
                    <ul class="list-line">
                        <li>- <b><i>характер сервисов</i></b>, в получении которых сообщество заинтересовано, – внутренних, клубных («для своих») или внешних, общественных («для всех», включая не-членов сообщества);</li>
                        <li>- <b><i>баланс между внутренними сервисами</i></b> (удовлетворяющими текущие нужды членов сообщества и потенциально приносящими ресурсы) и общественной деятельностью, легитимирующей сообщество в глазах внешних стейкхолдеров и дающее ему широкую базу поддержки.</li>
                    </ul>
                    <ul>
                        <li><b><i>Самоопределение</i></b> сообщества, осознание собственных границ и критериев, по которым тот или иной субъект может быть принят в сообщество (или исключен из него). Можно выделить два подхода к работе с границами сообщества: условно «мягкий» и «жесткий». <br>
                            <b><i>Особенностями «мягкого» подхода</i></b> к определению границ сообщества являются:</li>
                    </ul>
                    <ul class="list-line">
                        <li>- <b><i>Низкие барьеры входа</i></b> или, по факту, их отсутствие. Например, членом сообщества может считаться – «явочным порядком» – любой специалист, так или иначе причастный к проблематике финансовой грамотности. Это может быть школьный учитель, прошедший соответствующее обучение в методическом центре Проекта. Или журналист, подписанный на группу в социальных сетях с новостями о финансовой грамотности для прессы.</li>
                        <li>- <b><i>«Естественные» механизмы восходящей мобильности</i></b> внутри сообщества: кто более активен, тот в первую очередь и получает статусные и иные преимущества, создаваемые сообществом.</li>
                        <li>- <b><i>Значительная свобода действий</i></b> членов в сочетании со слабой управляемостью и недостаточным ресурсным обеспечением – совокупность этих факторов ставит под вопрос устойчивость сообщества, его способность быстро реагировать на возникающие вызовы и производить сколько-нибудь сложные сервисы.</li>
                        <li>- Как следствие: <b><i>слабая связность сообщества</i></b>, особенно большого – почти любая инфраструктура будет нерелевантной аморфной общности, со слабо проявленными интересами участников.</li>
                    </ul>
                    <ul>
                        <li><b><i>Особенностями «жесткого» подхода</i></b> к определению границ сообщества являются:</li>
                    </ul>
                    <ul class="list-line">
                        <li>- <b><i>Сравнительно высокие барьеры входа</i></b>, связанные как минимум с необходимостью самозаявления, но чаще – с прохождением экспертного скрининга (например, профессиональной биографии или произведенных продуктов), получением рекомендаций/приглашений от действующих членов сообщества или даже с профессиональной аттестацией (например, на соответствие профессиональному стандарту). Возможны варианты, когда доступ в сообщество по самозаявлению закрыт
                            и осуществляется только по приглашению, например, в рамках внутренней ротации.</li>
                        <li>- <b><i>Иерархичность и более оформленная структура</i></b>, по крайней мере предполагающая восходящую мобильность и диверсифицированный доступ к сервисам, производимым сообществом. Нередко – платность тех или иных сервисов или статусных продвижений.</li>
                        <li>- <b><i>Наличие внутренних правил</i></b>, систем стимулирования и санкций обеспечивает (как правило) стабильность сообщества, однако зарегулированность затрудняет «прорастание» новых идей, а также вступает в противоречие с разнообразием членов сообщества (если оно присутствует), запросов и интересов.</li>
                        <li>- Как следствие: <b><i>более высокая связность и закрытость</i></b> сообщества, в т.ч. для баланса потребностей инфраструктуры и ресурсов на ее содержание. Закрытые сообщества нередко носят корпоративный характер и их общественная значимость не всегда очевидна внешним стейкхолдерам.</li>
                    </ul>
                    <p>Ввиду размеров и неоднородности сегментов формирующегося сообщества профессионалов
                        в области финансовой грамотности, применительно к нему целесообразно рассматривать комбинацию «мягкого» и «жесткого» подходов. Далее описанные модельные характеристики прикладываются к сообществу профессионалов в области финансовой грамотности.</p>
                </section>



                <section class="section">
                    <h3 id="ex3">3. ПРЕДЛОЖЕНИЯ ДЛЯ СООБЩЕСТВА ПРОФЕССИОНАЛОВ В ОБЛАСТИ ФИНАНСОВОЙ ГРАМОТНОСТИ</h3>
                    <h4>3.1. ЦЕЛЬ, ЗАДАЧИ И ГРАНИЦЫ СООБЩЕСТВА</h4>
                    <p>Оценка текущего состояния сообщества и его стейкхолдеров, включая спрос на сообщество (раздел 1 Концепции), учет опыта существующих сообществ (раздел 2 Концепции) позволяет предложить следующую <b><i>цель развития и укрепления профессионального сообщества: создание конкурентных преимуществ, дополнительных возможностей для своих членов, усиливаемых «положительным» естественным отбором.</i></b></p>
                    <p>Реализация данной цели будет полезной для стейкхолдеров и общества в целом: они получат более качественные услуги, повестка финансовой грамотности будет развиваться, в т.ч. на длинном временнóм горизонте, выходящем за рамки 2020 г.</p>
                    <p><b><i>Задачи по развитию сообщества профессионалов</i></b> в области финансовой грамотности, исходя из заявленной цели, можно сформулировать следующим образом:</p>
                    <ul>
                        <li><b><i>Консолидация профессионального сообщества</i></b>, содействие в обретении им собственной организационной структуры и публичных представителей, выстраивание процедур повсе- дневного функционирования, включая правила включения/исключения из сообщества.
                            Первоочередными здесь являются следующие подзадачи:</li>
                    </ul>
                    <ul class="list-line">
                        <li>- <b><i>определение границ сообщества</i></b>, т.е. внятных правил входа/присоединения, в т.ч.
                            квалификационных (компетенции, опыт, конструктивный вклад в распространение финансовой грамотности);</li>
                    </ul>
                    <ul class="list-line">
                        <li>- <b><i>формирование стимулов</i></b> для привлечения и удержания членов сообщества, в т.ч. <b><i>«лидеров мнений»</i></b>, причем с потенциально разными точками зрения на конкретные аспекты финансовой грамотности, но с более или менее единым пониманием целей и задач функционирования сообщества в целом.</li>
                    </ul>
                    <ul>
                        <li><b><i>Предоставление профессиональному сообществу поддержки за счет формирования сервисной инфраструктуры</i></b>, работающей на нужды членов сообщества (внутренние сервисы) и на нужды стейкхолдеров и/или общества в целом (внешние сервисы).
                    </ul>
                    <p><b><i>Внутренние сервисы</i></b> направлены прежде всего на:</p>
                    <ul class="list-line">
                        <li>- удовлетворение текущих нужд своих членов;</li>
                        <li>- создание стимулов, мотивирующих специалистов входить в сообщество (в т.ч. системы статусов, рейтингов, грейдов);</li>
                        <li>- развитие инфраструктуры, снижающей издержки по осуществлению профессиональной деятельности, обеспечивающей связность и консолидированность сообщества;</li>
                        <li>- поддержание горизонтальной коммуникации, в т.ч. для обмена опытом и «лучшими практиками».</li>
                    </ul>
                    <p><b><i>Внешние сервисы</i></b> позволяют <b><i>позиционировать сообщество</i></b> в пространстве стейкхолдеров (заинтересованных пользователей), вести <b><i>продуманную пиар-кампанию</i></b>, а также осуществлять деятельность, которая полезна как стейкхолдерам/обществу, так и потенциально может консолидировать сообщество изнутри. Примерами такого рода <b><i>«консолидирующей» деятельности</i></b> могут быть:</p>
                    <ul class="list-line">
                        <li>- деятельность, связанная с профессиональными и образовательными стандартами (включая школьные и вузовские ФГОСы);</li>
                        <li>- экспертная поддержка Межведомственной координационной комиссии по реализации Стратегии повышения финансовой грамотности в Российской Федерации на 2017-2023 гг.;</li>
                        <li>- развитие системы целевых показателей (KPI,в т.ч. опирающихся на Рейтинг финансовой грамотности регионов России) по отбору и оценке результативности проектов по финансо- вой грамотности на уровне регионов России;</li>
                        <li>- разработка и поддержание стандарта профессиональной деятельности в сфере финансовой грамотности.</li>
                    </ul>
                    <p>Представленные примеры <b><i>«консолидирующей» деятельности потенциально</i></b> могут связывать разные сегменты профессионального сообщества (в различных комбинациях) между собой и на практике выполнять интегрирующую роль, создавая основу для горизонтальной коммуникации и предпосылки для институционализации сообщества.<br>
                        Сервисы для сообщества рассматриваются далее в <span class="blue-p">параграфе 3.2</span> Концепции.</p>

                    <ul>
                        <li><b><i>Разработка механизмов, обеспечивающих устойчивость сообщества</i></b> и самостоятельности его деятельности на средне и долгосрочном временном горизонте.<br>
                            Подходы к решению данной задачи предлагаются в <span class="blue-p">параграфе 3.3</span> Концепции.</li>
                    </ul>

                    <h4>Границы сообщества и привлечение его членов</h4>
                    <p>Анализ текущего состояния сообщества профессионалов в области финансовой грамотности, спроса на него (раздел 1 К онцепции) и модельных характеристик сообщества (раздел 2 Концепции), с одной стороны, а также конфигурация стоящих перед сообществом задач, с другой стороны, позволяют предложить вариант <b><i>двойной границы сообщества – мягкой внешней и достаточно жесткой внутренней.</i></b></p>
                    <ul>
                        <li>«Мягкая» внешняя граница позволит включить всех сопричастных к проблематике финансовой грамотности (среди тех, разумеется, кто выразит соответствующее желание или не заявит возражения). «Мягкая» граница сможет «явочным» порядком включить в себя тысячи специалистов в области финансовой грамотности (например, многочисленный сегмент учителей). <br>
                            Первоначальное приглашение участников может осуществляться посредством рассылки электронных писем по закрытому реестру физических и юридических лиц, работающих по проблематике финансовой грамотности в России.</li>
                        <li>«Жесткая» внутренняя граница позволит выделить «ядро» сообщества – «суперпрофессионалов» в сфере финансовой грамотности в России, максимум несколько сотен специалистов.</li>
                    </ul>
                    <img class="section-img" src="concept4.png">
                    <ul>
                        <li>Доступ к сервисам может ранжироваться в зависимости от «местоположения» эксперта – во «внешнем» контуре или во «внутреннем ядре», – а также от артикулируемого им запроса (т.е. сервис предоставляется не «по умолчанию», а по обоснованному запросу).</li>
                        <li>Разделение членов сообщества между «внешним» контуром и «внутренним ядром» может проводиться:</li>
                    </ul>
                    <ul class="list-line">
                        <li>- по соответствию профессиональному стандарту (прохождение экспертами сертификации);</li>
                        <li>- по приглашению/рекомендации;</li>
                        <li>- на основании совокупности активностей эксперта как по проблематике финансовой
                            грамотности в целом, так и в рамках сообщества в частности;</li>
                        <div class="yellow-block">
                            Речь может идти в т.ч. о присвоении статусов, наборе квалификационных баллов за различные виды активности, а потенциальным членам сообщества (или претендентам на вхождение в «ядро») могут предлагаться стандартные «дорожные карты» по их накоплению. Например, прохождение внутренней сертификации, пополнение Библиотеки Портала, рецензирование материалов, выкладываемых в Библиотеку, посещение/выступление на мероприятиях, консультирование других членов сообщества.
                        </div>
                        <li>- по критерию производства собственного экспертного знания (наличию собственного опыта в сфере финансовой грамотности), а не трансляции чужого.</li>
                    </ul>
                    <ul>
                        <li>Сообщество может формировать для проработки тех или иных вопросов рабочие группы, состоящие из специалистов «ядра». Это выделит дополнительный – еще более узкий, с численностью несколько десятков человек максимум, – круг наиболее активных и вовлеченных экспертов, а также обеспечит внутреннюю ротацию внутри «ядра».</li>
                    </ul>

                    <p><b><i>Стимулы для вхождения</i></b> в сообщество (а также для самостоятельного продвижения в его «внутреннее ядро») должны быть настроены таким образом, чтобы эксперт имел возможность <b><i>быстрее/качественнее решить свою задачу внутри сообщества</i></b>, нежели вовне его. Подобные стимулы – если обретут популярность и станут массовыми – будут развивать механизмы внутренней координации сообщества и повышать его устойчивость. Среди таких стимулов могут быть:</p>
                    <ul>
                        <li>Набор сервисов для поддержки повседневной деятельности эксперта. Эти сервисы должны объединять сообщество и, по возможности, не приводить к конфликту интересов (стратегия win-win), например:</li>
                    </ul>
                    <ul class="list-line">
                        <li>- доступ к современным и актуальным базам знаний, к системам горизонтальной коммуникации и обмена опытом;</li>
                        <li>- доступ к быстрому и аккумулированному информированию о новостях / мероприятиях в сфере финансовой грамотности, релевантных сегменту;</li>
                        <li>- возможность оперативно получать ответ (экспертизу) на свой вопрос;</li>
                        <li>- доступ к базе контактов экспертов в области финансовой грамотности.</li>
                    </ul>
                    <ul>
                        <li>Возможность взаимодействовать с государственными органами, межведомственными комиссиями и рабочими группами, предоставление им оплачиваемой экспертизы, возможность включать в повестки обсуждения важные для того или иного сегмента сообщества пункты.</li>
                    </ul>
                    <ul>
                        <li>Возможность фандрайзинга ресурсов – финансовых, человеческих, информационных – что особенно актуально из-за неопределенности, связанной с поддержкой проблематики финансовой грамотности после 2020 г.</li>
                    </ul>
                    <ul>
                        <li>Статусы – внутри сообщества и вовне его – повышающие экспертную и общественную значимость специалиста; возможность получать статусные оценки (например, благодарности) от стейкхолдеров (например, органов власти).</li>
                    </ul>
                    <ul>
                        <li>Продвижение/популяризация собственных разработок и проектов с помощью сообщества.</li>
                    </ul>
                    <ul>
                        <li>Профессиональный рост, в т.ч. получающий формализованную оценку (сертификат о
                            соответствии профессиональному стандарту, например).</li>
                    </ul>
                    <ul>
                        <li>Работающий механизм разрешения споров, купирования конфликтных ситуаций за счет многообразия медиаторских (третейских) процедур, наличия внутренних правил и стандартов поведения.</li>
                    </ul>
                    <ul>
                        <li>Возможная институционализация сообщества в будущем и перспектива вхождения члена сообщества в органы управления.</li>
                    </ul>
                    <p>Вопрос институционализации (юридического оформления) сообщества в том или ином виде сам по себе может выступать консолидирующим фактором. Он должен обсуждаться профессионалами в области финансовой грамотности на мероприятиях и прорабатываться в формате аналитических и организационно-правовых документов.</p>
