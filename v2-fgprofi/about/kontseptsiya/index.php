                        <h3 id="ex4">3.2. Сервисы для сообщества профессионалов в области финансовой грамотности</h3>
                        <div class="section__button"></div>
                    </div>
                    <div class="section__content">

                        <p><b><i>Наличие сервисов</i></b> для членов (а также для стейкхолдеров и общества в целом) имеет системообразующее значение для построения сообщества, т.к. тесно связано со стимулами вступать в него (см. параграф 3.1 Концепции), признанием и статусом сообщества в целом. Сервисная составляющая – важный элемент всех проанализированных сообществ. Исходя из текущего состояния сообщества профессионалов в области финансовой грамотности (раздел 1) и задач по его развитию (параграф 3.1 Концепции), можно выделить следующие виды сервисов.</p>
                        <p class="under">Коммуникация и информирование</p>
                        <p>Активность сообщества должна быть направлена на стимулирование взаимодействия/общения между экспертами: существует запрос на обмен информацией и опытом по разработке материалов, участие в проектах, просвещение населения, повышение собственного экспертного статуса, связь с представителями тех или иных стейкхолдеров, получение доступа к экспертам-«звездам» (возможности обратиться и получить ответ). Развитие внутренней коммуникации – особенно на системной основе – стимулирует ротацию внутри сообщества и восходящую мобильность экспертов. Формируется общий запрос на коммуникационную площадку, <b><i>на которой могло бы осуществляться экспертное взаимодействие и информирование</i></b> – обособленную и с регулируемым доступом (выбор конкретного варианта площадки может производиться позднее за рамками Концепции).</p>
                        <p>В первую очередь коммуникационная площадка позволит реализовать следующие возможности для представителей профессионального сообщества:</p>
                        <ul>
                            <li>Получение информации и консультаций по следующим направлениям:</li>
                        </ul>
                        <ul class="list-line">
                            <li>- по вопросам использования материалов, находящимся в Библиотеке Портала (в том
                                числе, о новых и/или обновленных материалах);</li>
                            <li> - по вопросам взаимодействия с экспертами/организациями, работающими по проблематике финансовой грамотности в России (исходя из информации, содержащейся в реeстре физических и юридических лиц, работающих по проблематике финансовой грамотности в России);</li>
                            <li>- по вопросам участия в подготовке учебных материалов для школьников к включению в электронные ресурсы учебных материалов;</li>
                            <li> - по методическим вопросам финансовой грамотности и защиты прав потребителей финансовых услуг (при необходимости, путем перенаправления вопроса профильному эксперту/организации из реестра физических и юридических лиц, работающих по проблематике финансовой грамотности в России);</li>
                            <li>- по вопросам участия в мероприятиях, проводимым в рамках Контракта, направленных на обмен опытом и распространение лучших практик Проекта;</li>
                            <li> - по работе коммуникационной площадки;</li>
                            <li> - по вопросам участия (актуализации информации) в реестре физических и юридических лиц, работающих по проблематике финансовой грамотности в России.</li>
                        </ul>
                        <ul>
                            <li>Размещение на коммуникационной площадке ответов на типовые запросы профессионального сообщества.</li>
                            <li>Сбор, обобщение и анализ запросов (вопросов) представителей профессионального сообщества и ответов на них.</li>
                            <li>Информирование профессионального сообщества по запросам Минфина России и Разработчика Портала.</li>
                            <li>Обеспечение возможности для представителей профессионального сообщества получать и отправлять сообщения другим представителям Профессионального сообщества.</li>
                            <li>Обеспечение возможности поиска нужной персоны среди членов сообщества.</li>
                            <li>Возможность публикации собственного контента, относящегося к деятельности Професси-онального сообщества.</li>
                        </ul>
                        <p>Впоследствии функционал коммуникационной площадки может быть расширен за счет разработки и подключения следующих возможностей:</p>
                        <ul>
                            <li>Создание <b><i>личных профилей</i></b> представителей сообщества для отделения профессионального профиля от социального в соцсетях (запрос явным образом высказывался экспертами) и обеспечения возможности для приватного/непубличного общения;</li>
                            <li><b><i>Проведение опросов</i></b> членов сообщества, голосований, в т.ч. по отдельным группам специалистов;</li>
                            <li><b><i>Уведомление</i></b> членов сообщества о конкурсах, стажировках, обучающих курсах;</li>
                            <li>Осуществление <b><i>«умных» рассылок;</i></b></li>
                            <li>Создание <b><i>«умного календаря»</i></b> событий в виде единого таймлайна с возможностью настройки – например, выбора региональных или федеральных мероприятий, целевой аудитории, типа (онлайн или офлайн). Важны наличие гибкой фильтрации мероприятий, настройки оповещений, развитие собственного «консьерж-сервиса» для содействия экспертам в регистрации на мероприятия и/или получения материалов по их результатам;</li>
                            <li>Реализация процедур <b><i>управления сообществом и координации</i></b> различных его структур.</li>
                        </ul>
                        <p>Онлайн-коммуникация может дополняться офлайн-площадкой для очного общения – нетворкинга для обмена опытом и информацией, наращивания личных связей, знакомства с экспертами- «звездами». Для представителей отдельных сегментов сообщества (например, учителей) важен сам факт очной публичной
                            презентации их достижений: поощрение за заслуги, возможность делиться экспертным мнением и получать за такую активность некие бонусы. Можно выделить два подхода к выбору тем мероприятия: </p>

                        <ul>
                            <li>Развитие Профессионального сообщества как такового; вовлечение новых членов.</li>
                            <li>Конкретные сюжеты по финансовой грамотности (или в связи с ней), находящиеся в фокусе внимания экспертов (организация взаимодействия в связи с «горячими темами» в сфере фи- нансовой грамотности).</li>
                        </ul>
                        <p class="under">Экспертиза</p>
                        <p>Существует спрос на базу знаний/практик для профессионалов в сфере финансовой грамотности, причем, предусматривающую возможности комментирования, рейтингования, оценивания материалов. Сообщество (отдельные его представители) может осуществлять фильтрацию «входящего» потока информации, чтобы далее распространялась информация, прошедшая хотя бы минимальный контроль качества и соответствия тематике. Особый интерес как для потенциальных членов сообщества, так и для стейкхолдеров представляет информирование о результатах реализованных проектов в сфере финансовой грамотности.</p>
                        <p>Могут быть рассмотрены следующие сервисы, связанные с экспертизой в сфере финансовой грамотности:</p>
                        <ul>
                            <li>Облегчение поиска и обмена информацией;</li>
                            <li>Доступ к обзорно-аналитическим материалам по актуальным темам финансовой грамотности. Принципы отбора тем для материалов могут быть следующими:</li>
                        </ul>
                        <ul class="list-line">
                            <li>- тема интересна и/или полезна представителям профессионального сообщества;</li>
                            <li> - тема востребована у целевой аудитории, с которой работает профессионал;</li>
                            <li>- тема стимулирует дискуссию среди профессионалов;</li>
                            <li>- тема способствует развитию профессионального сообщества</li>
                        </ul>
                        <ul>
                            <li>Доступ к обзорно-реферативным материалам, позволяющим получить представление о материалах Портала.</li>
                        </ul>
                        <p>Отдельным направлением экспертизы может быть экспертное сопровождение органов исполнительной власти, межведомственных комиссий и рабочих групп в связи с реализацией ими Национальной стратегии и соответствующего Плана мероприятий (в частности, речь может идти о содействии работе Межведомственной координационной комиссии по реализации Стратегии). Потенциал профессионального сообщества может быть использован при выполнении Минфином России, например, следующих положений Стратегии и Плана мероприятий:</p>
                        <ul>
                            <li>Актуализация содержания учебных программ и учебно-методических материалов образовательных курсов по основам финансовой грамотности с учетом развития финансового рынка и нормативно-правовой базы (п. 1.25);</li>
                            <li>Осуществление систематической методической поддержки педагогических работников, реализующих в своей деятельности программы повышения финансовой грамотности (п. 1.26);</li>
                            <li>Обобщение и распространение информации о наилучших практиках и инициативах в области повышения финансовой грамотности и защиты прав потребителей финансовых услуг (п. 1.35);</li>
                        </ul>
                        <p>Для включения профессионального сообщества в реализацию Стратегии и Плана мероприятий могут быть предприняты следующие действия:</p>
                        <ul>
                            <li>создание рабочих групп из членов профессионального сообщества и представителей органов власти;</li>
                            <li>презентация доклада о ходе реализации стратегии на ежегодной конференции, организуемой членами профессионального сообщества, с последующим обсуждением;</li>
                            <li>подключение рабочих групп к мониторингу состояния реализации Стратегии;</li>
                            <li>включение докладов рабочих групп в ежегодный доклад (в т.ч. в виде приложений).</li>
